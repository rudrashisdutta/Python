import math

radius=7
length=6
breadth=2
side=3
pi=math.pi

print("Circle:\nPerimeter:", 2 * pi * radius,"\nArea:",pi * radius ** 2)
print("\nRectangle:\nPerimeter:", 2 * (length + breadth),"\nArea:",length * breadth)
print("\nSquare:\nPerimeter:", 4 * side,"\nArea:",side ** 2)